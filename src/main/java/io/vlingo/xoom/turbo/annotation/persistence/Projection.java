import java.lang.annotation.Target;

@Target(ElementType.TYPE)
@Retention(RetentionPolicy.CLASS)
public @interface Projection {

  @SuppressWarnings("rawtypes")
  Class<? extends StateStoreProjectionActor> actor();

  Class<?>[] becauseOf();

}
